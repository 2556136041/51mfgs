</p>
<p style="margin-top: 1.5em; margin-bottom: 1.5em; line-height: 1.8; font-size: 18px; text-align: justify;">
    楼市回暖也得益于近期各地出台的稳楼市政策。3月11日，深圳市出台政策，提出要推进各项业务线上办理，加快商品房网签进度，简化业务流程，提高商品房网签效率；3月23日，长春发布文件明确，对已缴存商品房预售资金且依法依规诚信经营的房地产开发企业，如有新建或者续建项目，允许申请提取监管账户资金金额不超过50%的资金用于工程建设；武汉近期也推出了相应举措，包括提高预售监管资金使用效率、完善社保个税缴纳的购房认定标准等。
</p>
<p style="margin-top: 1.5em; margin-bottom: 1.5em; line-height: 1.8; font-size: 18px; text-align: justify;">
    “随着国内疫情得到有效控制，预计后期还会有部分城市出台针对供应端的调控政策，缓解房企资金面压力。”诸葛找房数据研究中心分析师国仕英说。
</p>

