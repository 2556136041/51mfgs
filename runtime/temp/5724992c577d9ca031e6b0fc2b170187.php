	<h1>让每一次馈赠，都成为美好记忆</h1>
	<img id="img1" src="__img__/gb_about.jpg" alt="" />
	<p>几年前，我从一本书上看到freecycle、craigslist这样的国外闲置物品馈赠平台，他们都是由一些程序员搭建维护，不收取会员任何费用，倡导节约节能，会员间可以自由转送物品。赠人玫瑰，手有余香，也许在你看来是没用的东西，但对于别人来说却视为珍宝。这样的平台之所以受到欢迎，不仅在于平台的公益性，更在于在它所传承的理念是与整个现代社会追求绿色节能的理念相一致，是一件造福于大众的好事。当我看到这样的平台，我感到由衷的高兴，并想为此做点什么。</p>
    <p>为此，我做了一些专门的准备工作，比如学习编程，了解一个网站的搭建、管理、运营。比如通过日常交流、观察和调查，了解人们对这一平台的需求。通过两年时间的筹备，终于有了这个平台的面世。</p>
    <p>我们生活在一个物质生活丰足的年代，新品永远是人们乐此不疲的话题。而旧物常常被人们束之高搁，或丢弃，尽管在当初购置时也是花了大价钱的。面对这些数目庞大、有着循环利用价值的旧物像垃圾一样扔掉，不得不说是一种巨大的社会资源浪费。不少一线城市居民近几年也许都有切身的体会：原来搬家或换新家具家电时还能找到收废品的人，让旧家具、旧家电进入二手市场。然而现在不仅找不到收废品的，小区没有地方也不允许业主随便乱扔。最后不得不花钱请人上门处理。</p>
    <p>实际上，我们准备扔掉的这些物品完全可以被其他人利用。但由于信息不对称，馈赠者和领取者之间无法建立联系。如果有这样的平台，是不是可以实现资源的共享，让旧物进入一种良性的可循环利用呢！
    <p>今天，搭建一个免费馈赠平台，在技术上已不是什么问题，但要想让其大行其道却不是件容易的事，关键是观念问题。在很多人看来，宁愿丢掉旧物也不愿给别人，一怕麻烦，二是心理障碍。但我相信，随着时间的推移，人们的观念也会有所改变。就拿我身边的人来说，当我说服一个老板把他闲置的名牌钢笔、笔记本电脑拿出来馈赠给山区大学生时，他很乐意。当我的女儿得知我在弄这样一个平台时，她表示要把自己八成新的芭比娃娃和玩具全部放在网上，给需要的人索取。</p>
    <p>我建议：当你准备丢弃一件在你看来觉得仍有价值或者估计还会有人需要时，不妨把它发到这个平台上，说不定能找到需要它的人。当你舍不得扔掉的旧物带着你的体温和记忆送到某个人的手上时，你会不会有一种释然之感油然而生，哦！终于找到它的归宿了。</p>
